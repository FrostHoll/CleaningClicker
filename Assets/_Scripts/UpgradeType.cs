/// <summary>
/// Вид улучшения
/// </summary>
public enum UpgradeType
{
    PerClick,           // увеличивает сумму по клику
    PassivePer3Sec,     // увеличивает сумму по 3-х секундному таймеру
    PassivePer5Sec,     // увеличивает сумму по 5-и секундному таймеру
    Helper              // увеличивает кол-во рабочих в личной команде
}
